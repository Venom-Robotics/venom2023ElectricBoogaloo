package org.firstinspires.ftc.teamcode.helper;

public class Utils {
    public static String fontText(String text, String color) {return "<font color='" + color + "'>" + text + "</font>";}
    public static String fontText(String text, String color, String size) {return "<font color='" + color + "' size='" + size + "'>" + text + "</font>";}
    public static String fontText(String text, String color, String size, String face) {return "<font color='" + color + "' size='" + size + "' face='" + face + "'>" + text + "</font>";}
}
